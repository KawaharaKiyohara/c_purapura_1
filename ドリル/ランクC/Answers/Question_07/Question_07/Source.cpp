#include <iostream>
#include "lib.h"

using namespace std;

int main()
{
	//GenerateScore関数は40人の成績をランダムに生成して、
	//それを、score配列に格納します。
	//この配列を調べて平均点以下(平均点を含む)をとった生徒の数をcount変数に記録しなさい。
	//そしてその結果をAnswer関数に渡しなさい。
	//答えがあっていれば、正解のメッセージボックスが表示されます。

	int score[40];
	GenerateScore(score);

	int count = 0;
	//まずは平均点を計算する。
	int avg = 0;
	for (int i = 0; i < 40; i++) {
		avg += score[i];
	}
	avg /= 40;
	//平均点以下の生徒の数を数える。
	for (int i = 0; i < 40; i++) {
		if (score[i] <= avg) {
			count++;
		}
	}
	//答え合わせ。
	SubmissionAnswer(count);
	return 0;
}